package koreait.day01;

public class C01_Print {

	//프로그램 실행 - main 메소드가 필요합니다
	public static void main(String[] args) {
		System.out.println("하이!");	//문자열데이터
		System.out.println(3.14);	//실수데이터
		System.out.println(100);	//수식
		System.out.println(34+567);	//정수
		System.out.println(true);	//논리
		System.out.println("하이!" + "자바야~");	//문자열의+기능은?
		System.out.println("나는" + 23 + "세 입니다.");
		
		//ln이 있으면  줄바꿈, print()  메소드는 줄바꿈 안합니다.
		
	}
	
}
